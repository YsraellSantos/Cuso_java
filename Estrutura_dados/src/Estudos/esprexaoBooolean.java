package Estudos;

public class esprexaoBooolean {
    public static void main(String[] args) {
     
     
        /* Operadores relacionais
          > maior que
          < menor que
          >= mior ou igual
          <= menor ou igual
          == igual a
          != diferente de
        */

        int indade = 19;
        boolean resultado  = indade != 18;
        System.out.println(resultado);
        
        String nome = "Israel";
        String nome1 = "Anne";
        boolean res = nome == nome1;
        System.out.println(res);

        float nota = 9.4f;
        float nota1 =5.9f;
        boolean teste = nota != nota1;
        System.out.println(teste);

        char letra = '+';
        char Letra = '-';
        boolean letras = letra < Letra;
        System.out.println(letras);
   
      }

   }



